package com.operatorjava;

public class ArithmeticO {

	public static void main(String[] args) {
		// The Arithmatic operator.
		System.out.println("Arithmetic operators");
		int n1= 15;
		int n2=17;
		int add= n1+n2;
		int sub= n1-n2;
		int mult= n1*n2;
		int div=n1/n2;
		int mod=n1%n2;
		System.out.println("The Sum of the 15 and 17 is :"+ add);
		System.out.println("The Sub of the 15 and 17 is :"+sub);
		System.out.println("The mult of the 15 and 17 is :"+ mult);
		System.out.println("The div of the 15 and 17 is :"+div);
		System.out.println("The mod of the 15 and 17 is :"+mod);

	}

}
