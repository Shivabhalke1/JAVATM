package com.operatorjava;

public class RelationalO {

	public static void main(String[] args) {
		// Relation Operator
				System.out.println("Relation operators");
				int x=9; 
				int y=10 ;
				System.out.println(x<y);
				System.out.println(x>y);
				System.out.println(x==y);
				System.out.println(x!=y);
				System.out.println(x<=y);

	}

}
